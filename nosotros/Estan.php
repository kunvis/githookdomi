        <p class="titulo"><b>¿Cuáles son estos elementos a los que hay que atender en la gestión?</b></p>
        <p id="descrip"><p><b>Calidad del servicio: relación entre propuesta y expectativas</b></p> 
        <p>Esta calidad se determina en función de las expectativas del cliente objetivo. Una vez 
        definido el destinatario de la propuesta del restaurante, será él quien determinará si la 
        calidad es adecuada o no. </p>
        <p><b>Calidad de los procesos de la cocina</b></p>
        <p>Se refiere fundamentalmente a la producción de los platos.  Para gestionar este aspecto, 
        el gestor establecerá fichas y escándalos de producción de platos. También inciden en este
        componente de la calidad las materias primas y la selección de proveedores. Son elementos
        claves para asegurar la calidad de los platos, que son el corazón del restaurante. 
        La atención constante a las opiniones de los clientes es una forma de evaluar estos procesos. 
        Observar minuciosamente las quejas, devoluciones de platos, manifestaciones de desconformidad 
        de los clientes directamente o en redes sociales, es prioritario para lograr una propuesta de 
        calidad.</p>
        <p><b>Calidad de la atención al cliente</b></p>
        <p>En la satisfacción del cliente no solamente influyen los platos y la comida. El servicio, 
        el comportamiento del personal, la gestión de los problemas, todo es importante. Nada debe 
        quedar fuera de las previsiones. Los camareros son los puntos de contacto por excelencia 
        entre los clientes y el restaurante. Su imagen es un elemento por destacar. Su presentación,
        su forma de hablar, su amabilidad, el conocimiento sobre la carta, son determinantes del 
        servicio. Toda gestión de calidad debe tener presente que los camareros son la cara visible
        del restaurante. Hay diferentes estrategias para medir la calidad de la atención al cliente. 
        Los cuestionarios sobre la amabilidad de los camareros, tiempo de espera y servicio recibido, 
        son una útil herramienta. La observación de las opiniones en las redes sociales es otra.</p>
